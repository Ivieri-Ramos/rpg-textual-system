package br.com.rpg.model.enums;

/**
 * Enum para definir consumíveis presentes no jogo.
 * <p>
 */

public enum TipoConsumivel{
    /**
     * Consumível usado para curar o heroi
     */
    CURA,
    /**
     * Consumível usado para restaurar a mana do heroi
     */
    MANA,
    /**
     * Consumível usado para upar características do heroi, como força
     */
    BUFF,
    /**
     * Consumível usado para curar o heroi de efeitos negativos
     */
    ANTIDOTO,
}

